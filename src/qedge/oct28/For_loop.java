package qedge.oct28;

public class For_loop {
	

	public static void main(String[] args) {

		for(int i=1;i<=10;i++)
		{
			System.out.println("pavani::"+i);
		}
		System.out.println("======================================");
	
	for(int i=1;i<=10;i+=2)
	{
		System.out.println("pavani::"+i);
	}
	}
}


	
	
	
	
	
	