package qedge.oct30;

public class Parseint {

	public static void main(String[] args) {
		String str1="498";
		String str2="7";
		int x=Integer.parseInt(str1);
		int y=Integer.parseInt(str2);
		System.out.println(x/y);
		System.out.println(x+y);
		System.out.println(x%y);
		System.out.println(x-y);
		System.out.println(x*y);
		
				

	}

}
